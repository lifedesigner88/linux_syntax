SQL 3가지 구분

    DDL 
        Data Definition Language
        데이터베이스나 테이블 등을 생성, 삭제하거나 그 구조를 변경
        CREATE, ALTER, DROP    (테이블 통체로 날리기 DROP)
    DML
        Data Manipulation Language
        데이터 베이스에 저장된 데이터를 처리하거나 조회
        INSERT, UPDATE DELETE, SELECT 등 ()
    DCL
        Data Control Language  (마케팅 부서 SELECT 계정)
        사용자 권한 부여 등
        GRANT, REVOKE 등 (부여, 회수)

